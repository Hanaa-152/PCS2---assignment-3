from Bio import SeqIO

def parse_matrix(matrix):
    keys = matrix[0].split()
    return {(row.split()[0], k): int(v) for row in matrix[1:] for k, v in zip(keys, row.split()[1:])}

def glob(s, t, scores, gap):
    m, n = len(s), len(t)
    dp = [[i * gap if j == 0 else j * gap if i == 0 else 0 for j in range(n + 1)] for i in range(m + 1)]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            match = dp[i - 1][j - 1] + scores[(s[i - 1], t[j - 1])]
            dp[i][j] = max(match, dp[i - 1][j] + gap, dp[i][j - 1] + gap)
    return dp[m][n]

